async function editTodo(){
    const todoId = this.parentNode.dataset.id
    let newTodo = prompt('New Name')
    let newAddress = prompt('New Address')
    try{
        const response = await fetch('guest/editTodo', {
            method: 'put',
            headers: {'Content-type': 'application/json'},
            body: JSON.stringify({
                'todoIdFromJSFile': todoId,
                'todoValue': newTodo || this.parentNode.childNodes[0].innerText,
                'addressValue': newAddress || this.parentNode.childNodes[1].innerText
            })
        })
        const data = await response.json()
        console.log(data)
        location.reload()
    }catch(err){
        console.log(err)
    }
}

